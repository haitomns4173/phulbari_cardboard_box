package com.haitomns.phulbari;

public class Flower {
    private String name;
    private String imageName;

    public Flower(String name, String imageName) {
        this.name = name;
        this.imageName = imageName;
    }

    public String getName() {
        return name;
    }

    public String getImageName() {
        return imageName;
    }
}
